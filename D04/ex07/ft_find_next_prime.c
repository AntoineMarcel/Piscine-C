int		ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1 || nb < 0)
		return (2);
	while (i < nb)
	{
		if ((nb % i) != 0)
			i++;
		else
		{
			nb += 1;
			i = 2;
		}
	}
	return (nb);
}
