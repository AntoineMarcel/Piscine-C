int		ft_atoi(char *str)
{
	int neg;
	int nb;

	neg = 1;
	nb = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
	{
		str++;
	}
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
		{
			neg = -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		nb = nb * 10;
		nb = (*str - '0') + nb;
		str++;
	}
	return (neg * nb);
}
